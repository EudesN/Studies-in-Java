public class DadosPrimitivos {
    public static void main(String[] args){
        // capazes de armazenar valores inteiros
        byte a; // valores muito pequenos (até 128)
        short b; //  dado numérico inteiro de 16 bits (2 bytes), que pode armazenar valores entre -32.768 e 32.767
        int c;
        long d; // apra números extremamente longos
        
        // para valores decimais
        float e;
        double f;

        boolean g; // true ou false
        char h = 'a'; // caracter único
    }
}
